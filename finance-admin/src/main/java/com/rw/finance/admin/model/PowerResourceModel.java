package com.rw.finance.admin.model;

import java.util.List;

public class PowerResourceModel {
    // 资源编号
    private Long resourceid;

    // 父级编号
    private Long parentid;

    // 资源名称
    private String resourcename;

    // 资源地址
    private String resourceurl;

    // 资源图标
    private String resourceico;

    // 排序编号
    private Long sortno;

    // 类型
    private Integer type;

    // 状态
    private Integer status;

    // 备注
    private String remark;

    // 是否选中
    private Boolean selected;

    // 下级资源
    private List<PowerResourceModel> SubResources;

    public Long getResourceid() {
        return resourceid;
    }

    public void setResourceid(Long resourceid) {
        this.resourceid = resourceid;
    }

    public Long getParentid() {
        return parentid;
    }

    public void setParentid(Long parentid) {
        this.parentid = parentid;
    }

    public String getResourcename() {
        return resourcename;
    }

    public void setResourcename(String resourcename) {
        this.resourcename = resourcename;
    }

    public String getResourceurl() {
        return resourceurl;
    }

    public void setResourceurl(String resourceurl) {
        this.resourceurl = resourceurl;
    }

    public String getResourceico() {
        return resourceico;
    }

    public void setResourceico(String resourceico) {
        this.resourceico = resourceico;
    }

    public Long getSortno() {
        return sortno;
    }

    public void setSortno(Long sortno) {
        this.sortno = sortno;
    }

    public Integer getType() {
        return type;
    }

    public void setType(Integer type) {
        this.type = type;
    }

    public Integer getStatus() {
        return status;
    }

    public void setStatus(Integer status) {
        this.status = status;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }

    public Boolean getSelected() {
        return selected;
    }

    public void setSelected(Boolean selected) {
        this.selected = selected;
    }

    public List<PowerResourceModel> getSubResources() {
        return SubResources;
    }

    public void setSubResources(List<PowerResourceModel> subResources) {
        SubResources = subResources;
    }
}
