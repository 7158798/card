package com.rw.finance.admin.model;

import java.util.List;

/**
 * 激活码曲线图统计
 */
public class CountActvcodeEChartsModel {
    /**
     * 日期集合
     */
    private List<String> date;

    /**
     * 新增个数
     */
    private List<Long> addcount;

    /**
     * 使用个数
     */
    private List<Long> usecount;

    public List<String> getDate() {
        return date;
    }

    public void setDate(List<String> date) {
        this.date = date;
    }

    public List<Long> getAddcount() {
        return addcount;
    }

    public void setAddcount(List<Long> addcount) {
        this.addcount = addcount;
    }

    public List<Long> getUsecount() {
        return usecount;
    }

    public void setUsecount(List<Long> usecount) {
        this.usecount = usecount;
    }
}
