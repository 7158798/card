package com.rw.finance.admin.model;

import java.util.List;

/**
 * 代理收益曲线图统计
 */
public class CountAgentProfitEChartsModel {
    /**
     * 日期集合
     */
    private List<String> date;

    /**
     * 合计收益
     */
    private List<Double> totalamount;

    /**
     * 还款收益
     */
    private List<Double> repayamount;

    /**
     * 激活收益
     */
    private List<Double> activeamount;

    public List<String> getDate() {
        return date;
    }

    public void setDate(List<String> date) {
        this.date = date;
    }

    public List<Double> getTotalamount() {
        return totalamount;
    }

    public void setTotalamount(List<Double> totalamount) {
        this.totalamount = totalamount;
    }

    public List<Double> getRepayamount() {
        return repayamount;
    }

    public void setRepayamount(List<Double> repayamount) {
        this.repayamount = repayamount;
    }

    public List<Double> getActiveamount() {
        return activeamount;
    }

    public void setActiveamount(List<Double> activeamount) {
        this.activeamount = activeamount;
    }
}
